package com.example.baitapgiuaki;

public class cacbaihat {
    private String tenbaihat;
    private String tencasi;
    private int imgcasi;
    private int imgbacham;

    public cacbaihat(String tenbaihat, String tencasi, int imgcasi, int imgbacham) {
        this.tenbaihat = tenbaihat;
        this.tencasi = tencasi;
        this.imgcasi = imgcasi;
        this.imgbacham = imgbacham;
    }

    public String getTenbaihat() {
        return tenbaihat;
    }

    public void setTenbaihat(String tenbaihat) {
        this.tenbaihat = tenbaihat;
    }

    public String getTencasi() {
        return tencasi;
    }

    public void setTencasi(String tencasi) {
        this.tencasi = tencasi;
    }

    public int getImgcasi() {
        return imgcasi;
    }

    public void setImgcasi(int imgcasi) {
        this.imgcasi = imgcasi;
    }

    public int getImgbacham() {
        return imgbacham;
    }

    public void setImgbacham(int imgbacham) {
        this.imgbacham = imgbacham;
    }
}
